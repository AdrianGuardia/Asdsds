    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed

        String usuario=jTextFieldUsuario.getText();
        String password= new String(jPasswordFieldPassword.getPassword());
        
        
        
        
        if(usuario.length()>0 && password.length()>0)
        {
            try{
                
                Conectar cc= new Conectar();
                Connection cn= cc.conexion();
                
                Statement st =  cn.createStatement();
                ResultSet sql = st.executeQuery("SELECT * FROM admin");
                boolean found = false;
                
                while (sql.next()){
                    if(usuario.equals(sql.getString("usuario")) && password.equals(sql.getString("password"))){
                        found=true;
                        break;
                    }
                }
                
                if (found==false){
                    JOptionPane.showMessageDialog(null,"Contrase√±a Incorrecta!", "Houston tenemos un problema!", JOptionPane.DEFAULT_OPTION);
                }else{

                    myHome v = new myHome();
                    v.setVisible(true);
                this.setVisible(false);
                
                }
            }catch(SQLException e){
                Logger.getLogger(miLogin.class.getName()).log(Level.SEVERE, null, e);
            }            
        }else{
        JOptionPane.showMessageDialog(null,"No debes dejar ning√∫n espacio en blanco", "Houston tenemos un problema!", JOptionPane.DEFAULT_OPTION);
        }

    }//GEN-LAST:event_jButtonLoginActionPerformed

