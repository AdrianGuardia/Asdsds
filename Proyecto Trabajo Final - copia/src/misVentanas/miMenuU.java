    private void jButtonAñadirPlatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAñadirPlatoActionPerformed
        String nombre= jComboBoxNombre.getSelectedItem().toString();
        Plato p= new Plato(nombre);
        p.generarCant();
        p.generarPrecio();
        refP.insertarPlato(p);

        jComboBoxNombre.setSelectedIndex(0);
        jLabelCantPlatos.setText(String.valueOf(refP.getnP()));
    }//GEN-LAST:event_jButtonAñadirPlatoActionPerformed

    private void jButtonAñadirPedidoAleaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAñadirPedidoAleaActionPerformed

        if (refP.getnP()==4) {
            refP.montoTotalAPagar();
            listM.agregarPedido_MozoAlea(refP);
            refP= new Pedido();
        }else{
            JOptionPane.showMessageDialog(null,"Debe completar el pedido");
        }

    }//GEN-LAST:event_jButtonAñadirPedidoAleaActionPerformed

