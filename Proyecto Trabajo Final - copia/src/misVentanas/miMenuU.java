    private void jButtonAñadirPedidoEspActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAñadirPedidoEspActionPerformed
        String nombre= jTextFieldApellido.getText();
        if (refP.getnP()==4) {

            refP.montoTotalAPagar();
            listM.agregarPedido_MozoEsp(refP, nombre);
            refP= new Pedido();
        }else{
            JOptionPane.showMessageDialog(null,"Debe completar el pedido");
        }
        jTextFieldApellido.setText("");
    }//GEN-LAST:event_jButtonAñadirPedidoEspActionPerformed

